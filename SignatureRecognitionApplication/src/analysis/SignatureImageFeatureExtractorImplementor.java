	private int countBlackPixels(Mat matrix) {
		double [] pixel;
		boolean b = false;
		int numberOfBlackPixels = 0;
						
		for (int j = 0; j < matrix.cols(); j++) {
			for (int k = 0; k < matrix.rows(); k++) {		
					pixel = matrix.get(k, j);							
					b = pixel[0] == 0.0 ? true : false;		
					if (b)
						numberOfBlackPixels++;		
			}
		}
		return numberOfBlackPixels;
	}

	

